days = ('Monday', 'Tuesday', 'Wednesday', 'Thursday')
print(days)
print(type(days))

#accessing items in a tuple
print(days[3])
print(days[-2])

#to find the length
print(len(days))

#index method - returning the index of a value
print(days.index('Thursday'))

#count - returns the occurrences of a specified value
print(days.count('Monday'))